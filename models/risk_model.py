def get_category_scores(controls):
    """Считает средний балл по каждой категории для радара"""
    scores = {}
    for category in CATEGORIES:
        category_controls = [c for c in controls if c["category"] == category]
        if category_controls:
            avg = sum(c["score"] for c in category_controls) / len(category_controls)
            scores[category] = round(avg, 2)
        else:
            scores[category] = 0
    return scores


def get_risk_level(score):
    """Возвращает уровень риска по баллу"""
    if score >= 4:
        return "Низкий", "#2ecc71"
    elif score >= 2.5:
        return "Средний", "#f39c12"
    else:
        return "Высокий", "#e74c3c"


def get_overall_score(controls):
    """Считает общий балл по всем контролям"""
    if not controls:
        return 0
    return round(sum(c["score"] for c in controls) / len(controls), 2)