def main():
    if not os.path.exists(DATA_FILE):
        print(f"Ошибка: файл не найден — {DATA_FILE}")
        print("Запустите приложение хотя бы один раз, чтобы файл данных был создан.")
        return

    wb = load_workbook(DATA_FILE)

    # ── Лист «Настройки» ──────────────────────────────────────────────────────
    ws1 = wb["Настройки"]
    ws1.delete_rows(1, ws1.max_row)          # очищаем всё
    for key, value in SETTINGS:
        ws1.append([key, value])
    print(f"[Настройки]  записано {len(SETTINGS)} строк")

    # ── Лист «Риски» ─────────────────────────────────────────────────────────
    ws3 = wb["Риски"]
    if ws3.max_row > 1:
        ws3.delete_rows(2, ws3.max_row - 1)  # удаляем данные, заголовок (строка 1) остаётся
    for risk in RISKS:
        ws3.append(list(risk))
    print(f"[Риски]      записано {len(RISKS)} записей")

    # ── Сохранение ───────────────────────────────────────────────────────────
    wb.save(DATA_FILE)
    print(f"[Файл]       сохранён: {DATA_FILE}")
    print("Готово. Перезапустите приложение для загрузки тестовых данных.")


if __name__ == "__main__":
    main()
